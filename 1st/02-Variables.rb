saludos = "Hola mundo!" # Ruby usa inferencia, por lo que el intérprete se encarga
# de determinar el tipo de dato de las variables por nosotros.

puts saludos # Imprimirá el valor en "saludos". No hacemos uso de ";".

saludos = 50 # Ruby es de "tipado dinámico", por lo que podemos hacer esto...

puts saludos # y el valor anterior de "saludos" será reemplazado por este otro.